<?php

namespace Entity;

/**
 * Charlab
 */
class Charlab
{
    /**
     * @var integer
     */
    private $idcharla;

    /**
     * @var string
     */
    private $Id;

    /**
     * @var string
     */
    private $Fecha;


    /**
     * Get idcharla
     *
     * @return integer
     */
    public function getIdcharla()
    {
        return $this->idcharla;
    }

    /**
     * Set id
     *
     * @param string $id
     *
     * @return Charlab
     */
    public function setId($id)
    {
        $this->Id = $id;

        return $this;
    }

    /**
     * Get id
     *
     * @return string
     */
    public function getId()
    {
        return $this->Id;
    }

    /**
     * Set fecha
     *
     * @param string $fecha
     *
     * @return Charlab
     */
    public function setFecha($fecha)
    {
        $this->Fecha = $fecha;

        return $this;
    }

    /**
     * Get fecha
     *
     * @return string
     */
    public function getFecha()
    {
        return $this->Fecha;
    }
}
